package entity;

import java.sql.Date;

/**
 *
 * @author ADMIN
 */
public class Blog {
    int id;
    String name,image,content;
    Date time;

    public Blog() {
    }

    public Blog(int id, String name, String image, String content, Date time) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.content = content;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
    
    
}
